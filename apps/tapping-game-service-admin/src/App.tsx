import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { ClickEventList } from "./clickEvent/ClickEventList";
import { ClickEventCreate } from "./clickEvent/ClickEventCreate";
import { ClickEventEdit } from "./clickEvent/ClickEventEdit";
import { ClickEventShow } from "./clickEvent/ClickEventShow";
import { GameSessionList } from "./gameSession/GameSessionList";
import { GameSessionCreate } from "./gameSession/GameSessionCreate";
import { GameSessionEdit } from "./gameSession/GameSessionEdit";
import { GameSessionShow } from "./gameSession/GameSessionShow";
import { LeaderboardList } from "./leaderboard/LeaderboardList";
import { LeaderboardCreate } from "./leaderboard/LeaderboardCreate";
import { LeaderboardEdit } from "./leaderboard/LeaderboardEdit";
import { LeaderboardShow } from "./leaderboard/LeaderboardShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"TappingGameService"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="ClickEvent"
          list={ClickEventList}
          edit={ClickEventEdit}
          create={ClickEventCreate}
          show={ClickEventShow}
        />
        <Resource
          name="GameSession"
          list={GameSessionList}
          edit={GameSessionEdit}
          create={GameSessionCreate}
          show={GameSessionShow}
        />
        <Resource
          name="Leaderboard"
          list={LeaderboardList}
          edit={LeaderboardEdit}
          create={LeaderboardCreate}
          show={LeaderboardShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
